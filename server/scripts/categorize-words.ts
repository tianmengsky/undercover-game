function categorize(common: string, undercover: string): string {
  const text = common + undercover
  for (const [cat, keywords] of CATEGORY_RULES) {
    if (cat === '其他') continue
    if (keywords.some((kw) => text.includes(kw))) {
      return cat
    }
  }
  return '其他'
}

// ── main ──
const raw = fs.readFileSync(src, 'utf-8')
const lines = raw.trim().split('\n')
const header = lines[0].trim()

if (!header.startsWith('难度,普通词,卧底词')) {
  console.error('CSV 格式不符合预期，跳过')
  process.exit(1)
}

const outLines: string[] = ['难度,类型,普通词,卧底词']
const stats = new Map<string, number>()

for (let i = 1; i < lines.length; i++) {
  const line = lines[i].trim()
  if (!line) continue
  const parts = line.split(',')
  if (parts.length < 3) continue
  const difficulty = parts[0]
  const common = parts.slice(1, -1).join(',') // 处理含逗号的词
  const undercover = parts[parts.length - 1]

  // 简单拆：标准 CSV 是 难度,普通词,卧底词
  const csvMatch = line.match(/^([^,]+),([^,]+),(.+)$/)
  if (!csvMatch) {
    console.warn(`  ⚠ 跳过第 ${i + 1} 行: ${line}`)
    continue
  }

  const cat = categorize(csvMatch[2], csvMatch[3])
  stats.set(cat, (stats.get(cat) || 0) + 1)
  outLines.push(`${csvMatch[1]},${cat},${csvMatch[2]},${csvMatch[3]}`)
}

fs.writeFileSync(dst, outLines.join('\n') + '\n', 'utf-8')
console.log(`输出: ${dst}`)
console.log(`共 ${outLines.length - 1} 对词\n`)
console.log('类型分布:')
for (const [cat, n] of [...stats.entries()].sort((a, b) => b[1] - a[1])) {
  console.log(`  ${cat}: ${n}`)
}
